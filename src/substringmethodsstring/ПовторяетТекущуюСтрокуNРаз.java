package substringmethodsstring;

public class ПовторяетТекущуюСтрокуNРаз {
    public static void main(String[] args) {
        String string = "Сколько времени потребуется для тебя \n";

        String string1 = string.repeat(4);     // Повторяет текущую строку n раз

        System.out.println(string1);
    }
}
