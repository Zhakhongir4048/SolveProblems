package remainderofthedivision;

public class ОстатокОтДеления {
    public static void main(String[] args) {
        int count = 5 % 2;          // остаток от деления 1
        int count1 = 20 % 4;       // остаток от деления 0
        int count2 = 9 % 5;       //остаток от деления 4
        int count3 = 1 % 2;        // остаток от деления 1


        // ½ = 0.5. 1%2 = 0, а остаток 1 - 0*2 = 1.


        System.out.println(count + "||" + count1 + "||" + count2 + "||" + count3);


    }

}

